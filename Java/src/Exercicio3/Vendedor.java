package Exercicio3;
public class Vendedor {

    private String nome;

    private double SalarioFixo;

    private double TotalVendasMes;

    private double SalarioComissao;

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public double getSalarioFixo() {
        return SalarioFixo;
    }

    public void setSalarioFixo(double salarioFixo) {
        SalarioFixo = salarioFixo;
    }

    public double getTotalVendasMes() {
        return TotalVendasMes;
    }

    public void setTotalVendasMes(double totalVendasMes) {
        TotalVendasMes = totalVendasMes;
    }

    public double getSalarioComissao() {
        return SalarioComissao;
    }

    public void setSalarioComissao(double salarioComissao) {
        SalarioComissao = salarioComissao;
    }

    

}
